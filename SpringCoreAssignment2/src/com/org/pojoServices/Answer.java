package com.org.pojoServices;

public class Answer {
	String answers;

	
	public Answer() {
		super();
	}


	public Answer(String answers) {
		super();
		this.answers = answers;
	}
	

	public String getAnswers() {
		return answers;
	}

	public void setAnswers(String answers) {
		this.answers = answers;
	}
	
	

}
